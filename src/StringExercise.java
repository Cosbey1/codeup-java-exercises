public class StringExercise {
    public static void main(String[] args) {
        String attitude = "We dont need no education.";
//        System.out.println("We dont need no education.");
        attitude = attitude + " We dont need no thought control";
        System.out.println(attitude);

        String excitement = "Check \"this\" out! ,";
        excitement = excitement + " \"s inside of \" s!";
        System.out.println(excitement);

        String windows = "In windows,";
        windows = windows + " the main drive is usually C:\\ ";
        System.out.println(windows);

        String backslash = "I can do backslashes \\,";
        backslash = backslash + " double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(backslash);
    }
}

